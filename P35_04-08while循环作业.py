
#使用"#"号输出一个长方形,用户可以指定宽和高,如果长为3,宽为4,则输出一个横着有3个"#",竖着有4个"#"的长方形

'''
height = int(input("长:"))                               #赋值于一个变量,让用户定义一个值(长)                                       
width  = int(input("宽:"))                               #赋值于一个变量,让用户定义一个值(宽)                                      
num_height = 1                                           #赋值于一个定量,让整数1赋值于num_长                      

while height >= num_height:                              #当长>=num_长时;(可以看成:当长>=1时)
    num_width=1                 %赋值定义一个值,当宽循环完后,重新让整数1赋值于num_宽
   
    while width>=num_width:                              #当长>=num_宽时;(可以看成:当宽>=1时)
        print("#",end=" ")      ^语法:end换行,end=" "
        num_width +=1            
        
    print("#")
    num_height +=1

'''

