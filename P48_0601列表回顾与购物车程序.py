#1.列表的嵌套

name = ['MAC','book','bike','kinble']
a = [ [1,2,3],'alex',4,'2,3,4']

#赋值
b = a[0]                                #可以把a[0]的值赋给b
print(b)                                #>>>123

#修改值
a[0][2] = 4                             #a[大位置][小位置] “2级嵌套”
print(a)                                #>>>[[1, 2, 4], 'alex', 4, '2,3,4']

#2.列表的分别赋值

a,b = [2,3]
