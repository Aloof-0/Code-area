'''
1.用一个定量赋值于一个正方型,值不变               2.相同的语法:
num2 =4                                              num2=4
while num2>0:                                        while num2>0:
    print("####")                                        num=4
    num2 -=1                                             while num>0
                                                             print("#",end="")
                                                             num -=1  
                                                         print("#")
                                                         num2 -=1
'''


num2=4                                              #第一步:赋值
while num2>0:                                       #第二步:num2 = 4
    num=4                                           #第三步:赋值
    while num>0:                                    #第四步:num = 2
        print(num,end="")                           #第五步:不换行 打印一个#
        num -=1                                     #第六步:num = num - 1
    print()                                         #第七步:打印
    num2 -=1                                        #第八步:num2 = num2 - 1

    
